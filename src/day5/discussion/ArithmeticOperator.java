
package day5.discussion;


public class ArithmeticOperator {
    public static void main(String[] args) {
        int num = 50;
        int num2 = 10;
        
        System.out.println(num+num2);
        System.out.println(num-num2);
        System.out.println(num*num2);
        System.out.println(num/num2);
    }
}
